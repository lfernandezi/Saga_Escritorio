    public int validar2() {
        int resultado = 0;
        
        if (txtCodigo.getText().equals("") || txtCodigo.getText() == null
                || txtDescripcion.getText().equals("") || txtDescripcion.getText() == null
                || txtPrecio.getText().equals("") || txtPrecio.getText() == null
                || txtStockMin.getText().equals("") || txtStockMin.getText() == null
                || cboArea.getSelectedIndex() == 0 || cboProveedor.getSelectedIndex() == 0) {
            resultado = 1;
        }
        if (txtCodigo.getText().length() < 8 || txtCodigo.getText().length() > 8) {
            resultado = 2;
        }
     
        return resultado;
    }
