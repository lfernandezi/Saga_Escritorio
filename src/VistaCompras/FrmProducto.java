    private void btnIrActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIrActionPerformed
        // TODO add your handling code here:
        bloquearCasillas();
        activarCasillas();
        btnCancelar.setEnabled(true);

        String[] cabecera = {"Codigo", "Descripción", "Precio Unitario", "Stock Mínimo", "Area Producto", "Proveedor"};
        String[] cuerpo = new String[6];

        tablaProducto = new DefaultTableModel(null, cabecera);

        Connection cn = null;
        PreparedStatement ps = null;
        ResultSet rs = null;
        String sql = "select x.codProducto,x.descripcion,x.precioUnitario,x.stockMin,p.razonSocial,ap.nombreAreaProducto from producto x "
                + "inner join proveedor p on x.cod_proveedor=p.cod_proveedor "
                + "inner join area_producto ap on x.cod_Area_Producto=ap.cod_Area_Producto where codProducto=?";
        try {
            cn = Conexion.getConexion();
            ps = cn.prepareStatement(sql);
            ps.setString(1, txtCodigo.getText());
            rs = ps.executeQuery();
            if (rs.next()) {
                JOptionPane.showMessageDialog(null, "Registro encontrado");
                cuerpo[0] = rs.getString("codProducto");
                cuerpo[1] = rs.getString("descripcion");
                cuerpo[2] = rs.getString("precioUnitario");
                cuerpo[3] = rs.getString("stockMin");
                cuerpo[4] = rs.getString("nombreAreaProducto");
                cuerpo[5] = rs.getString("razonSocial");
                //cuerpo[6]=rs.getString("estadoProv"); 
                tablaProducto.addRow(cuerpo);

                tblProducto.setModel(tablaProducto);
                txtCodigo.setText("");;

            } else {
                JOptionPane.showMessageDialog(null, "Registro no encontrado");
                txtCodigo.setText("");
                btnIr.setEnabled(true);
            }
        } catch (SQLException ex) {
            Logger.getLogger(FrmProveedor.class.getName()).log(Level.SEVERE, null, ex);

            try {
                if (rs != null) {
                    rs.close();
                }
                if (ps != null) {
                    ps.close();
                }
                if (cn != null) {
                    cn.close();
                }
            } catch (Exception e2) {
                e2.printStackTrace();
            }
        }


    }//GEN-LAST:event_btnIrActionPerformed

