    private void tblProductoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProductoMouseClicked
        // TODO add your handling code here:
        try {
            int fila = tblProducto.getSelectedRow();
            String codigo = tblProducto.getValueAt(fila, 0).toString();
            Connection cn = null;
            PreparedStatement ps = null;
            ResultSet rs = null;
            cn = Conexion.getConexion();
            String sql = "select x.codProducto,x.descripcion,x.precioUnitario,x.stockMin,p.razonSocial,ap.nombreAreaProducto from producto x "
                    + "inner join proveedor p on x.cod_proveedor=p.cod_proveedor "
                    + "inner join area_producto ap on x.cod_Area_Producto=ap.cod_Area_Producto where codProducto=?";
            ps = cn.prepareStatement(sql);
            ps.setString(1, codigo);
            rs = ps.executeQuery();

            while (rs.next()) {
                txtCodigo.setText(String.valueOf(codigo));
                txtDescripcion.setText(rs.getString("descripcion"));
                txtPrecio.setText(rs.getString("precioUnitario"));
                txtStockMin.setText(rs.getString("stockMin"));
                cboArea.setSelectedItem(rs.getString("nombreAreaProducto"));
                cboProveedor.setSelectedItem(rs.getString("razonSocial"));

            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
        bloquearCasillas();
        txtCodigo.setEnabled(false);
        btnEditar.setEnabled(true);
        btnEliminar.setEnabled(true);
        btnCancelar.setEnabled(true);
    }//GEN-LAST:event_tblProductoMouseClicked

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int rspta = -1;
        bloquearCasillas();
        rspta = JOptionPane.showConfirmDialog(null, "¿Desea eliminar el registro?");
        if (rspta == 0) {
            txtOpcion.setText("3");
            Productos producto = new Productos();
            producto.setCodigo(txtCodigo.getText());
            producto.setOpcion(Integer.parseInt(txtOpcion.getText()));
            ControlProductos mxp = new ControlProductos();
            mxp.MantProd(producto);
        } else {
            JOptionPane.showMessageDialog(null, "Registro no fue eliminado");
        }

        mostrarTabla();
        btnCrear.setEnabled(true);
        btnBuscar.setEnabled(true);
        btnVerTabla.setEnabled(true);
        txtOpcion.setText("0");

    }//GEN-LAST:event_btnEliminarActionPerformed

